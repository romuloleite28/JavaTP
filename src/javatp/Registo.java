public class Registo {
    
    private String data;
    private String hora;
    private double temperatura;
    private String id_divisao;
    private String tipo_divisao;
    private String id_pessoa;
    private String tipo_pessoa; 

    public Registo(String data, String hora, double temperatura, String id_divisao, String tipo_divisao, String id_pessoa, String tipo_pessoa) {
        this.data = data;
        this.hora = hora;
        this.temperatura = temperatura;
        this.id_divisao = id_divisao;
        this.tipo_divisao = tipo_divisao;
        this.id_pessoa = id_pessoa;
        this.tipo_pessoa = tipo_pessoa;
    }
    

   
    
    
   
    
    
    
    
}
